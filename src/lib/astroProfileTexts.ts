/** Days in each month for a non-leap year; leap years handled separately for Feb. */
const DAYS_IN_MONTH = [0, 31, 29, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];

function isValidDate(year: number, month: number, day: number): boolean {
  if (month < 1 || month > 12) return false;
  if (day < 1) return false;
  // February: allow up to 29 only on leap years
  if (month === 2 && day === 29) {
    const isLeap =
      (year % 4 === 0 && year % 100 !== 0) || year % 400 === 0;
    return isLeap;
  }
  return day <= DAYS_IN_MONTH[month];
}

export function getSunSign(birthDate: string): ZodiacSign | null {
  // Must match yyyy-mm-dd exactly
  if (!/^\d{4}-\d{2}-\d{2}$/.test(birthDate)) return null;

  const parts = birthDate.split("-");
  const year = parseInt(parts[0], 10);
  const month = parseInt(parts[1], 10);
  const day = parseInt(parts[2], 10);

  if (isNaN(year) || isNaN(month) || isNaN(day)) return null;
  if (!isValidDate(year, month, day)) return null;

  if ((month === 3 && day >= 21) || (month === 4 && day <= 19)) return "牡羊座";
  if ((month === 4 && day >= 20) || (month === 5 && day <= 20)) return "金牛座";
  if ((month === 5 && day >= 21) || (month === 6 && day <= 20)) return "雙子座";
  if ((month === 6 && day >= 21) || (month === 7 && day <= 22)) return "巨蟹座";
  if ((month === 7 && day >= 23) || (month === 8 && day <= 22)) return "獅子座";
  if ((month === 8 && day >= 23) || (month === 9 && day <= 22)) return "處女座";
  if ((month === 9 && day >= 23) || (month === 10 && day <= 22)) return "天秤座";
  if ((month === 10 && day >= 23) || (month === 11 && day <= 21)) return "天蠍座";
  if ((month === 11 && day >= 22) || (month === 12 && day <= 21)) return "射手座";
  if ((month === 12 && day >= 22) || (month === 1 && day <= 19)) return "摩羯座";
  if ((month === 1 && day >= 20) || (month === 2 && day <= 18)) return "水瓶座";
  // Remaining valid dates: Feb 19–28/29, and Mar 1–20
  if (month === 2 || (month === 3 && day <= 20)) return "雙魚座";
  // Should never reach here after valid-date check, but satisfy TypeScript
  return null;
}

